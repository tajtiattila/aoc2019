func srcMap(t *testing.T, s string) *Map {
	i := strings.IndexByte(s, '\n')
	if i >= 0 {
		s = s[i+1:]
	}

	m, err := Parse(strings.NewReader(s))
	if err != nil {
		t.Fatal(err)
	}

	return m
}
